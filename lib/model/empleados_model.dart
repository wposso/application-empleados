class Empleado {
  final int id;
  final String nombre;
  final String documento;
  final int edad;
  final String direccion;
  final double salario;

  Empleado({
    required this.id,
    required this.nombre,
    required this.documento,
    required this.edad,
    required this.direccion,
    required this.salario,
  });
}
